from turtle import*
pensize(4)
speed(0)
setup(900,900)
setworldcoordinates(0,900,900,0)

def myArms():
    penup()
    hideturtle()
    goto(100, 150)
    pendown()
    begin_fill()
    fillcolor('gray')
    pencolor('gray')
    goto(250, 70)
    goto(180,30)
    goto(290,-30)
    goto(260,-70)
    goto(150,30)
    goto(230,70)
    goto(100,140)
    goto(100,150)
    end_fill()
    penup()
    pensize(1)
    goto(290,-30)
    begin_fill()
    pendown()
    goto(280,-40)
    goto(340,-50)
    goto(330,-60)
    goto(320,-60)
    goto(330,-55)
    goto(280,-40)
    end_fill()
    pensize(4)

    

    clone()
    penup()
    hideturtle()
    goto(-100, 150)
    pendown()
    begin_fill()
    fillcolor('gray')
    pencolor('gray')
    goto(-250, 70)
    goto(-180,30)
    goto(-290,-30)
    goto(-260,-70)
    goto(-150,30)
    goto(-230,70)
    goto(-100,140)
    goto(-100,150)
    end_fill()
    penup()
    pensize(1)
    goto(-290,-30)
    begin_fill()
    pendown()
    goto(-280,-40)
    goto(-340,-50)
    goto(-330,-60)
    goto(-320,-60)
    goto(-330,-55)
    goto(-280,-40)
    end_fill()




